    private void btnBancoModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBancoModificarActionPerformed
        CamposVacios = new VerificarCampos(txtNombreEmpresa, txtTelefonoBanco, comboDireccionBanco);

        if (bd.actualizarRegistro("banco",
                "nombre='" + this.txtNombreEmpresa.getText() + "', "
                + "direccion ='" + comboDireccionBanco.getSelectedIndex() + "', "
                + "telefono= '" + this.txtTelefonoBanco.getText() + "' ", "id_banco =" + this.labelBanco.getText())) {

            Mensaje("El registro de las Entidades ha sido actualizado con éxito.");
            refrescarGrillaBanco();

            CamposVacios.Vaciar_camposBank();
            txtTituloCliente.setText("Carga de Entidades Financieras");
            btnBancoAgregar.setEnabled(true);
            btnBancoModificar.setEnabled(false);
            btnBancoEliminar.setEnabled(false);
            this.txtNombreEmpresa.requestFocus();
            comboDireccionBanco.setSelectedIndex(0);

        } else {
            Mensaje("Ocurrio un error inesperado en el proceso, reintente.");
        }


        
        
    }//GEN-LAST:event_btnBancoModificarActionPerformed

