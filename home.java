    private void grBrancoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_grBrancoMouseClicked
    
        //al darle un click cambiar el texto
        if (evt.getClickCount() == 1) {
            accion = true;
            txtTituloBanco.setText("Carga de Entidad Financiera");
            btnBancoAgregar.setEnabled(true);
            btnBancoModificar.setEnabled(false);
            btnBancoEliminar.setEnabled(false);
            this.txtNombreEmpresa.requestFocus();
                       
            txtNombreEmpresa.setText("");
            txtTelefonoBanco.setText("");
            comboDireccionBanco.setSelectedIndex(0);
     
           
        }
        //al darle doble click
        accion = false;
        if (evt.getClickCount() == 2) {
            txtTituloBanco.setText("Modificar Entidad Finaciera");
            btnBancoModificar.setEnabled(true);
            btnBancoEliminar.setEnabled(true);
            btnBancoAgregar.setEnabled(false);
            //obtiene todos los valores de la grilla seleccionada
            int fila = this.grBranco.getSelectedRow();
            if (fila >= 0) {

                //almacena el valor de la cedula pára buscar el codigo del cliente
                int telefono = Integer.parseInt(String.valueOf(this.grBranco.getValueAt(fila, 2)));
                this.txtNombreEmpresa.setText(String.valueOf(this.grBranco.getValueAt(fila, 0)));
                this.txtTelefonoBanco.setText(String.valueOf(this.grBranco.getValueAt(fila, 2)));
                String combo = String.valueOf(this.grBranco.getValueAt(fila, 1));
                this.comboDireccionBanco.setSelectedIndex(valordelCombo(combo,"id_direccion,calle","direccion"));
                labelBanco.setText(String.valueOf(darCodigo(telefono,"id_banco,telefono ","banco")));
                //labelBanco.setVisible(false);
                txtNombreEmpresa.requestFocus();

            } else {
                Mensaje("Debe seleccionar el registro para continuar");
            }
            this.txtNombreCompleto.requestFocus();

        }        
        
        
        
        
        
        
        
    }//GEN-LAST:event_grBrancoMouseClicked

