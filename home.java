    private void grTarjetasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_grTarjetasMouseClicked
        CamposVacios = new VerificarCampos(txtClave, txtClave, txtClave, comboTarjetaEntidadesFinancieras, 
                comboTarjetasClientes, jFechaExpediciòn, jDateExpiracion, txtClave, estado, tipoTarjeta);
         
        //al darle un click cambiar el texto
        if (evt.getClickCount() == 1) {
            accion = true;
            txtTituloTarjetas.setText("Carga de Tarjetas");
            btnTarjetasAgregar.setEnabled(true);
            btnTarjetasModificar.setEnabled(false);
            btnTarjetasEliminar.setEnabled(false);
            this.txtNroTarjeta.requestFocus();
            CamposVacios.Vaciar_camposTarjeta();

        }
        //al darle doble click
        accion = false;
        if (evt.getClickCount() == 2) {
            txtTituloTarjetas.setText("Modificar Tarjetas");
            btnTarjetasAgregar.setEnabled(false);
            btnTarjetasModificar.setEnabled(true);
            btnTarjetasEliminar.setEnabled(true);

            //obtiene todos los valores de la grilla seleccionada
            int fila = this.grTarjetas.getSelectedRow();
            if (fila >= 0) {

                int nroTarjeta = Integer.parseInt(String.valueOf(this.grTarjetas.getValueAt(fila, 0)));
                lbTarjetaCodigo.setText(String.valueOf(darCodigo(nroTarjeta, "id_tarjeta,num_tarjeta", "tarjeta")));
                lbTarjetaCodigo.setVisible(false);
                this.txtNroTarjeta.setText(String.valueOf(this.grTarjetas.getValueAt(fila, 0)));

                String cliente = String.valueOf(this.grTarjetas.getValueAt(fila, 1));
                this.comboTarjetasClientes.setSelectedIndex(valordelCombo(cliente, "nro_cliente,nombre", "cliente"));

                this.txtClave.setText(String.valueOf(this.grTarjetas.getValueAt(fila, 2)));

                String estado1 = String.valueOf(this.grTarjetas.getValueAt(fila, 3));
                //JOptionPane.showMessageDialog(null, estado1);
                 if (estado1.equals(RadioButtonCredito.getActionCommand())){
                     RadioButtonCredito.setSelected(true);
                     RadioButtonCredito.setActionCommand("Credito");
                 }else if(estado1.equals(RadioButtonDebito.getActionCommand())){
                    RadioButtonDebito.setSelected(true);
                    RadioButtonDebito.setActionCommand("Debito");
                 }
                 this.txtLineaTarjeta.setText(String.valueOf(this.grTarjetas.getValueAt(fila, 4)));
                 this.txtSaldoTarjeta.setText(String.valueOf(this.grTarjetas.getValueAt(fila, 5)));
                 
                 SimpleDateFormat formatter = new SimpleDateFormat(jFechaExpediciòn.getDateFormatString());
                 String fecha1 = String.valueOf(this.grTarjetas.getValueAt(fila, 6));
                 String fecha2 = String.valueOf(this.grTarjetas.getValueAt(fila, 7));
                  try {
                    Date date1 = formatter.parse(fecha1);
                    jFechaExpediciòn.setDate(date1);
                    Date date2 = formatter.parse(fecha2);
                    jDateExpiracion.setDate(date2);
                   
                } catch (Exception e) {
                }
                String entidad = String.valueOf(this.grTarjetas.getValueAt(fila, 8));
                this.comboTarjetaEntidadesFinancieras.setSelectedIndex(valordelCombo(entidad, "id_banco,nombre", "banco"));
                
                String estado = String.valueOf(this.grTarjetas.getValueAt(fila, 9));
                if (estado.equals(RadioButtonActivo.getActionCommand())){
                     RadioButtonActivo.setSelected(true);
                     RadioButtonActivo.setActionCommand("Activo");
                 }else if(estado.equals(RadioButtonInativo.getActionCommand())){
                    RadioButtonInativo.setSelected(true);
                    RadioButtonInativo.setActionCommand("Inactivo");
                 }else if (estado.equals(RadioButtonBloqueado.getActionCommand())){
                    RadioButtonBloqueado.setSelected(true); 
                    RadioButtonBloqueado.setActionCommand("Bloqueado");
                 }
                

            } else {
                Mensaje("Debe seleccionar el registro para continuar");
            }
            this.txtNombreCompleto.requestFocus();


       }
        
        
        
        
    }//GEN-LAST:event_grTarjetasMouseClicked

