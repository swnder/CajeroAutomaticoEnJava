    private void grClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_grClienteMouseClicked
       
        //al darle un click cambiar el texto
        if (evt.getClickCount() == 1) {
            accion = true;
            txtTituloCliente.setText("Carga de Cliente");
            btnclienteAgregar.setEnabled(true);
            btnclienteModificar.setEnabled(false);
            btnclienteEliminar.setEnabled(false);
            this.txtNombreCompleto.requestFocus();
            
            txtBarrioCiudad.setText("");
            txtNombreCompleto.setText("");
            txtTelefono.setText("");
            txtCedula.setText("");

            comboDireccion.setSelectedIndex(0);
        }
        //al darle doble click
        accion = false;
        if (evt.getClickCount() == 2) {
            txtTituloCliente.setText("Modificar Cliente");
            btnclienteModificar.setEnabled(true);
            btnclienteEliminar.setEnabled(true);
            btnclienteAgregar.setEnabled(false);
            //obtiene todos los valores de la grilla seleccionada
            int fila = this.grCliente.getSelectedRow();
            if (fila >= 0) {

                this.txtCedula.setText(String.valueOf(this.grCliente.getValueAt(fila, 0)));
                //almacena el valor de la cedula pára buscar el codigo del cliente
                int cedula = Integer.parseInt(String.valueOf(this.grCliente.getValueAt(fila, 0)));
                this.txtNombreCompleto.setText(String.valueOf(this.grCliente.getValueAt(fila, 1)));
                this.txtTelefono.setText(String.valueOf(this.grCliente.getValueAt(fila, 3)));
                
                String combo = String.valueOf(this.grCliente.getValueAt(fila, 2));
                
                this.comboDireccion.setSelectedIndex(valordelCombo(combo,"id_direccion,calle","direccion"));
                lbCodigoCliente.setText(String.valueOf(darCodigo(cedula, "nro_cliente,cedula", "cliente")));
                lbCodigoCliente.setVisible(false);

            } else {
                Mensaje("Debe seleccionar el registro para continuar");
            }
            this.txtNombreCompleto.requestFocus();


       }
    }//GEN-LAST:event_grClienteMouseClicked

    public int darCodigo (int valorAcomparar, String campos, String tabla){
        /*
        bloque de codigo para buscar en la base de datos y devolver el valor de
        la clave ide
        
        */
         ResultSet buscar=null;
        int codigo=1;
        //nro_cliente,cedula
        buscar=bd.dameLista("select "+campos+" from "+tabla);
            
            try {
                buscar.beforeFirst();
                   
                while(buscar.next()){
                
                    if(buscar.getInt(2)==(valorAcomparar)){
                        
                            codigo = buscar.getInt(1);
                            break;
                        
                        
                    }
                }
            } catch (SQLException ex) {
                Mensaje("Error a la conexción de la base de datos");
            }
        
               
        return codigo; 
    }
    
    
    public int valordelCombo(String delCombo, String campo, String tabla ){
        
       /*
        bloque de codigo para buscar en la base de datos y devolver el valor de
        la clave ide
        
        */
         ResultSet buscar=null;
        int valor=0;
        buscar=bd.dameLista("select "+campo+" from "+tabla);
            
            try {
                buscar.beforeFirst();
                   
                while(buscar.next()){
                
                    if(buscar.getString(2).equals(delCombo)){
                        valor = buscar.getInt(1);
                        break;
                    }
                }
            } catch (SQLException ex) {
                Mensaje("Error a la conexción de la base de datos");
            }
        
        
        return valor;
    }
    
    
