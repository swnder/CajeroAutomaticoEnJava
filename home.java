    public void actualizarGrillaTransaccion(){
        //cargar en la tabla        
        if (bd.estaConectado()) {
            String [] columna={"z.id_transaccion","t.num_tarjeta",
                " z.cliente", "z.tipo_tarjeta ","z.linea","z.saldo",
                "b.nombre"," t.estado","z.fecha","z.monto","j.nombre"};
            String[] columnas = {"Nro", "Tarjeta", "Cliente", "Tipo Tarjeta", 
                "Linea", "Saldo", "Banco", "Estado", "Fecha Movimiento", "Retirado", "cajero"};
            int[] ancho = {10, 10, 15, 10, 10, 15, 10, 8, 15, 10, 10};

            grilla.configurarmodelo(grMovimiento, columnas, ancho);

            String tabla = " from transaccion z";
            String union = " INNER JOIN tarjeta t ON z.tarjeta = t.id_tarjeta"
                    + " INNER JOIN cliente c ON t.cliente = c.nro_cliente"
                    + " INNER JOIN banco b   ON t.idbanco = b.id_banco "
                    + " INNER JOIN cajeros k ON z.cajero  = k.id_cajero"
                    + " INNER JOIN banco j   ON k.banco   = j.id_banco"
                    + " order by z.id_transaccion";

            grilla.actualizargrilla(bd, grMovimiento, tabla, columna, union);

            this.txtBuscar.requestFocus(true);

        } else {
            JOptionPane.showMessageDialog(null, "Conexion fallida!");
            
        }
        
    }
    
    
    
    
